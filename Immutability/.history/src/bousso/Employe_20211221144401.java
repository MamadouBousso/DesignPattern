public class Employe{
    
    private String prenom;
    
    private String nom;

    public Employe(String prenom, String nom) {
        this.prenom = prenom;
        this.nom = nom;
    }
    
    public String getPrenom() {
        return prenom;
    }
    
    public String getNom() {
        return nom;
    }
    
    
}